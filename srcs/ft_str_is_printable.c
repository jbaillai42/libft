int		ft_str_is_printable(char *str)
{
	int	i;
	int	val;

	i = 0;
	val = 1;
	while (str[i] != '\0' && val == 1)
	{
		if (!(str[i] >= 32 && str[i] <= 126))
		{
			val = 0;
		}
		i++;
	}
	return (val);
}
