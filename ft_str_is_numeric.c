int		ft_str_is_numeric(char *str)
{
	int i;
	int val;

	i = 0;
	val = 1;
	while (str[i] != '\0' && val == 1)
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			val = 0;
		}
		i++;
	}
	return (val);
}
